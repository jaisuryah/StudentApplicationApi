package com.Auth.model;


import jakarta.persistence.*;

@Entity

public class Model {

    @Id
    @GeneratedValue
    private int id;

    @Column(name = "user_name")
    private String user;

    private String pass;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
